<?php
// Генерация случайного числа от 1 до 100
$number = mt_rand(1, 100);
echo "Привет! Я загадал число от 1 до 100. Попробуйте его угадать!\n";

// Цикл для игры
while (true) {
    // Запрос ввода числа от пользователя
    echo "Введите ваше предположение: ";
    $guess = (int) fgets(STDIN);
    
    // Проверка введенного числа
    if (!is_numeric($guess)) {
        echo "Пожалуйста, введите число!\n";
    } elseif ($guess < $number) {
        echo "Слишком маленькое число. Попробуйте еще раз.\n";
    } elseif ($guess > $number) {
        echo "Слишком большое число. Попробуйте еще раз.\n";
    } else {
        echo "Поздравляю! Вы угадали число $number!\n";
        break; // Завершение игры
    }
}
?>
